#Arithemtic operations in python

#Addition 
print('Addition')
print('5 + 6 = ',5+6)
print('15 + 6 = ',15+6)
print()

#Subtraction
print('Subtraction')
print('5 - 6 = ',5-6)
print('15 - 6 = ',15-6)
print()

# Multiplication
print('Multiplication')
print('5 * 6 = ',5*6)
print('15 * 6 = ',15*6)
print()

#Division
print('Division')
print('5 / 6 = ',5/6)
print('15 / 6 = ',15/6)
print()

#Double division (gives output in integer form)
print('Double division')
print('5 // 6 = ',5//6)
print('15 // 6 = ',15//6)
print()

#Modulus
print('Modulus')
print('6 % 5 = ',6%5)
print('15 % 6 = ',15%6)
print()

#Exponential
print('Exponential')
print('5 ** 2 = ',5**2)
print('4 ** 3 = ',4**3)
print()

