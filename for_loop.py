#Implementation of simple for-loop
print("Printing numbers from 0 to 9 using for-loop:")
for i in range(10):
    print(i)

#iterating through a list using for-loop
print("\nIterating through list and printing each element in the list:")

mylist = ['Nishant',18,91.20]
for element in mylist:
    print(element)

#using statrt,end,step in for-loop
print("\nUsing start,end,step in for-loop")
for i in range(50,100,5):
    print(i,end=' ') #printing the numbers in one line 




